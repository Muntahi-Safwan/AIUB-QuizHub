import java.lang.*;

public class Person {
    String name;
    String studentId;
    String email;
    String password;
    String department;

    Person(String name, String studentId, String email, String password, String department) {
        this.name = name;
        this.studentId = studentId;
        this.email = email;
        this.password = password;
        this.department = department;
    }
}
